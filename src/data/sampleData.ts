import type { Project, WBSTask } from '../types'

const makeHours = (vals: number[]) => ({
  'Principal': vals[0] ?? 0,
  'Project Manager': vals[1] ?? 0,
  'Senior Engineer': vals[2] ?? 0,
  'Engineer': vals[3] ?? 0,
  'Designer': vals[4] ?? 0,
  'CADD': vals[5] ?? 0,
  'Clerical': vals[6] ?? 0,
})

const task = (
  id: string,
  phase: WBSTask['phase'],
  discipline: WBSTask['discipline'],
  taskName: string,
  hrs: number[],
  factor = 1.0,
  notes = ''
): WBSTask => ({
  id,
  phase,
  discipline,
  taskName,
  hours: makeHours(hrs),
  adjustedHours: makeHours(hrs.map(h => Math.round(h * factor))),
  factor,
  notes,
})

